unsigned long sh_ker_get_ureg(int task_id, long addr)
{
	struct task_struct *task = NULL;
	task = sh_ker_get_task(task_id);

	if (task == NULL || task->uregs == NULL)
	{
		return 0;
	}

	return sh_arch_ker_get_ureg(task, addr);
}

